package android.sulc.nasaapod.models;

public class APODModel {
    String copyright, date, explanation, title, url, hdurl, media_type, service_version;

    public String getCopyRight() {
        return copyright;
    }

    public String getDate() {
        return date;
    }

    public String getExplanation() {
        return explanation;
    }

    public String title() {
        return title;
    }

    public String getUrl() {
        return url;
    }

    public String getHdurl() {
        return hdurl;
    }

    public String getMedia_type() {
        return media_type;
    }

    public String getService_version() {
        return service_version;
    }
}
